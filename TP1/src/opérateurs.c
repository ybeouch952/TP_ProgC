#include <stdio.h>

int main() {
    int a = 16, b = 3;
    
    printf("a + b = %d\n", a + b);
    printf("a - b = %d\n", a - b);
    printf("a * b = %d\n", a * b);
    printf("a / b = %d\n", a / b);
    printf("a %% b = %d\n", a % b);
    printf("a == b : %d\n", a == b);
    printf("a > b : %d\n", a > b);
    return 0;
}
